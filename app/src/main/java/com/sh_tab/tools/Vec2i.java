package com.sh_tab.tools;

public class Vec2i {
    public int i;
    public int j;

    public Vec2i(){
        i = 0;
        j = 0;
    }

    public Vec2i(int i, int j){
        this.i = i;
        this.j = j;
    }

    public void set(int i, int j){
        this.i = i;
        this.j = j;
    }
}
